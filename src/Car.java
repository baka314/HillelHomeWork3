public class Car {
    int START_ELECTRICITY = 0;
    int START_COMMAND = 0;
    int START_FUEL_SYSTEM = 0;
    private int startElectricity() {
        return START_ELECTRICITY;
    }
    private int startCommand() {
        return START_COMMAND;
    }
    private int startFuelSystem(){
        return START_FUEL_SYSTEM;
    }
}
